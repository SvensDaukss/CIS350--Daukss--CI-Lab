def add_numbers(a, b):
    return a + b


def multiply_numbers(a, b):
    return a * b


def subtract_numbers(a, b):
    return a - b


def divide_numbers(a, b):
    return a / b


def calc_sum(a, b, c):
    return a + b + c


def calc_product(a, b, c):
    return a * b * c


if __name__ == "__main__":
    print("Adding:", add_numbers(2, 4))
    print("Subtracting:", subtract_numbers(9, 2))
    print("Multiplying:", multiply_numbers(2, 4))
    print("Adding:", divide_numbers(2, 4))
    print("Sum:", calc_sum(2, 4, 7))
    print("Product:", calc_sum(2, 4, 7))
